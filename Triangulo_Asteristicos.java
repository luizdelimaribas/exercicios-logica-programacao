//Imprima um triângulo de asteriscos (padrão de loops)

public class Triangulo_Asteristicos {
    public static void main(String[]args){
        
        int linhas = 10, j, i;
        
     for( i = 0; i < linhas; i ++){

      for(j = 0; j < i; j ++){
        System.out.print("* ");
      }
      
     System.out.println("*");

     }

    }
    
}
